<h3>Bienvenido al sistema de convocatorias</h3>
<div class="pl-5 pr-5 pt-2 text-dark" style="position: relative; height: 480px; margin-top: .5rem; overflow: auto;">
    <h4 id="item-1">Reseña Histórica</h4>
    <div class="text-center pt-1">
        <a><i class="fas fa-book-open fa-7x"></i></a>
    </div>  
    <p class="p-2">
        La carrera de Licenciatura en Ingeniería de Sistemas entró en funcionamiento el año 1997
         a través de la Resolución Rectoral R.R. N° 634/97 del 13 de agosto de 1997 y de la Resolución de 
         Consejo Universitario N° 27/99 del 13 de Mayo de 1999 con la perspectiva de formar profesionales 
         calificados en el área de Ciencias de la Computación.
    </p>
    <h4 class="pt-4" id="item-2">Misión</h4>
    <div class="text-center pt-2">
        <a><i class="fas fa-rocket fa-7x"></i></a>
    </div>  
    <p class="p-2">
        Formar profesionales a nivel de licenciatura con sólida formación ética y cultural,
        comprometidos con los cambios tecnológicos, científicos y humanísticos, aportando
        constantemente al mejoramiento y creación de técnicas y métodos de los sistemas 
        computacionales.
    </p>
    <h4 class="pt-4" id="item-3">Visión</h4>
    <div class="text-center pt-1">
        <a><i class="fas fa-eye fa-7x"></i></a>
    </div>
    <p class="p-2">
        Ser una carrera de excelencia reconocida en el medio, dedicada a formar profesionales 
        a nivel de licenciatura, capaces de resolver problemas que involucren tecnología 
        computacional, con habilidad para administrar sistemas, proporcionar apoyo técnico, 
        desarrollar y aplicar nuevos métodos y técnicas para la construcción de sistemas de 
        software, vinculados al avance científico y tecnológico, con valores éticos y 
        responsabilidad socio-cultural.
    </p>
    <h4 class="pt-4" id="item-4">Objetivos Generales</h4>
    <div class="text-center pt-1">
        <a><i class="fas fa-shoe-prints fa-7x"></i></a>
    </div>
    <p class="p-2">
        Incorporar en el currículum los nuevos desafíos de la educación superior para responder 
        las necesidades y desarrollo de la región y el país, en el ámbito de la alta competitividad 
        nacional e internacional revalorizando el conocimiento. <br>
        Incentivar la investigación científica y tecnológica de la carrera, en un ambiente favorable 
        a la cultura de investigación que responda a las necesidades de la comunidad universitaria y 
        las demandas en el desarrollo en la región y en el país, con calidad y pertinencia social.
    </p>
    <h4 class="pt-4" id="item-5">Perfil Profecional</h4>
    <div class="text-center pt-1">
        <a><i class="fas fa-user-tie fa-7x"></i></a>
    </div>
    <p class="p-2">
        El Ingeniero de Sistemas debe ser capaz de planificar, diseñar, modelar, optimizar, 
        implantar y administrar soluciones TIC viables multidisciplinarias con un enfoque sistémico, 
        dentro de un marco institucional y organizativo. Viable significa tener en cuenta la 
        infraestructura de hardware, el software necesario, la infraestructura de comunicaciones 
        y los aspectos de seguridad necesarios en un sistema de esta naturaleza. Multidisciplinaria 
        significa que debe ser capaz de coordinar grupos de trabajo para relacionarse con las 
        diferentes ramas del saber humano.
    </p>
    <h4 class="pt-3" id="item-6">Mercado de Trabajo</h4>
    <div class="text-center pt-1">
        <a><i class="fas fa-briefcase fa-7x"></i></a>
    </div>
    <p class="p-2">
        Empresas Industriales con actividades productivas cuyo proceso tecnológico tenga 
        suficiente complejidad (complejos azucareros, fábricas de plástico, industrias 
        lácteas, refinerías, textiles, etc.). <br>
        Empresas de telecomunicaciones que utilizan la computadora como medio de 
        comunicación y control de sus procesos.
    </p>
</div>